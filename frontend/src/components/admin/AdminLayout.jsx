export default function AdminLayout() {
  const { usuario, logout } = useAuth();
  const navigate = useNavigate();
  const [sidebarOpen, setSidebarOpen] = useState(false);
  const [dark, setDark] = useState(() => localStorage.getItem('theme') === 'dark');

  useEffect(() => {
    document.documentElement.setAttribute('data-theme', dark ? 'dark' : 'light');
    localStorage.setItem('theme', dark ? 'dark' : 'light');
  }, [dark]);

  const handleLogout = () => {
    logout();
    navigate('/admin/login');
  };

  const links = [
    { to: '/admin', label: 'Dashboard', icon: Icons.dashboard },
    { to: '/admin/eventos', label: 'Eventos', icon: Icons.eventos },
    { to: '/admin/formularios', label: 'Talleres', icon: Icons.formularios },
    { to: '/admin/qrs', label: 'Códigos QR', icon: Icons.qr },
    { to: '/admin/estudiantes', label: 'Estudiantes', icon: Icons.estudiantes },
    { to: '/admin/grados', label: 'Grados', icon: Icons.grados },
    { to: '/admin/secciones', label: 'Secciones', icon: Icons.secciones },
    { to: '/admin/asistencias', label: 'Asistencias', icon: Icons.asistencias },
    { to: '/admin/configuracion', label: 'Configuración', icon: Icons.configuracion },
  ];

  return (
    <div className="admin-shell">
      <div className={`sidebar ${sidebarOpen ? 'open' : ''}`}>
        <div className="sidebar-header">
          <div className="sidebar-logo">
            <svg width="32" height="32" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><path d="M12 2L2 7l10 5 10-5-10-5z"/><path d="M2 17l10 5 10-5"/><path d="M2 12l10 5 10-5"/></svg>
          </div>
          <div>
            <h2>Escuela de Padres</h2>
            <p>Panel Administrativo</p>
          </div>
        </div>
        <nav className="sidebar-nav">
          {links.map((link) => (
            <NavLink
              key={link.to}
              to={link.to}
              end={link.to === '/admin'}
              className={({ isActive }) => isActive ? 'active' : ''}
              onClick={() => setSidebarOpen(false)}
            >
              <span className="nav-icon">{link.icon}</span>
              <span className="nav-label">{link.label}</span>
            </NavLink>
          ))}
          <div className="sidebar-divider" />
          <button className="nav-logout" onClick={handleLogout}>
            <span className="nav-icon">{Icons.logout}</span>
            <span className="nav-label">Cerrar Sesión</span>
          </button>
        </nav>
      </div>

      {sidebarOpen && <div className="sidebar-backdrop" onClick={() => setSidebarOpen(false)} />}

      <div className="main-content">
        <div className="topbar">
          <div className="topbar-left">
            <button className="menu-toggle" onClick={() => setSidebarOpen(!sidebarOpen)}>
              <svg width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"><line x1="3" y1="12" x2="21" y2="12"/><line x1="3" y1="6" x2="21" y2="6"/><line x1="3" y1="18" x2="21" y2="18"/></svg>
            </button>
            <h1>Panel de Control</h1>
          </div>
          <div className="topbar-right">
            <button className="theme-toggle" onClick={() => setDark(!dark)} title={dark ? 'Modo claro' : 'Modo oscuro'}>
              {dark ? Icons.sun : Icons.moon}
            </button>
            <div className="topbar-user">
              <div className="avatar">{usuario?.nombre?.charAt(0)?.toUpperCase() || 'A'}</div>
              <div className="user-info">
                <span className="user-name">{usuario?.nombre}</span>
                <span className="user-rol">{usuario?.rol}</span>
              </div>
            </div>
          </div>
        </div>

        <Outlet />
      </div>
    </div>
  );
}
